# Deskripsi: Menghitung nilai gradien garis

# Definisi dan Spesifikasi
# Gradien : 2 integer --> real
#   Gradien(dx,dy) menghitung nilai gradien dari garis dengan asumsi dx tidak 0

# Realisasi
def Pengurangan(dx,dy):
    return dx-dy
def Perkalian(x,y):
    return x*y
def Pembagian(x,y):
    return x/y
def Pertambahan(x,y):
    return x+y

# Aplikasi
print(Pengurangan(4,5))
print(Pengurangan(3,1))
print(Pengurangan(1,7))
print(Perkalian(200,500))
print(Pembagian(4,5))
print(Pertambahan(20,56))
print(Pembagian(25,7))